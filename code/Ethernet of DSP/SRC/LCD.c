void LCD_Initial(void)
{
	unsigned char i;				//用于循环写入指令
	RST_0();						//复位液晶屏
	DELAY_US(25000);
	RST_1();						//复位完成
	for(i=0;i<12;i++)				//开始写入指令
	{
		Write_Command_or_Data(Instruction[i],Command);			//写入指令
	}
}
