/****************
 * 描述：计算故障码
 * 输入：环号
 * 输出：码值
 * 返回值：-1:正常	1->接触不良（线路）	2->电弧异常	3->漏电	4->过压	5->欠压 6->过载 */
int Cal_LCD_ErrorCode(u8 SensorID)
{
	switch(SensorID)
	{
	case 1:
		if(Err_Code&(uint32)0x00000400) //第1路未接入(接触不良)
			return fault_Poor_contact;
		if(Err_Code&(uint32)0x00001000) //第1路电弧
			return fault_arc_fault;
		if(Err_Code&(uint32)0x00008000) //第1路漏电
			return fault_leakage;
		if(Err_Code&gBIT13)
			return fault_overvoltage;
		if(Err_Code&gBIT11)
			return fault_undervoltage;
		if(Err_Code&gBIT14)
			return fault_overload;
		break;
	case 2:
		if(Err_Code&(uint32)0x00040000) //第2路未接入
			return fault_Poor_contact;
		if(Err_Code&(uint32)0x00100000) //第2路电弧
			return fault_arc_fault;
		if(Err_Code&(uint32)0x00800000) //第2路漏电
			return fault_leakage;
		if(Err_Code&gBIT21)
			return fault_overvoltage;
		if(Err_Code&gBIT19)
			return fault_undervoltage;
		if(Err_Code&gBIT22)
			return fault_overload;
		break;
	case 3:
		if(Err_Code&(uint32)0x04000000) //第3路未接入
			return fault_Poor_contact;
		if(Err_Code&(uint32)0x10000000) //第3路电弧
			return fault_arc_fault;
		if(Err_Code&(uint32)0x80000000) //第3路漏电
			return fault_leakage;
		if(Err_Code&gBIT29)
			return fault_overvoltage;
		if(Err_Code&gBIT27)
			return fault_undervoltage;
		if(Err_Code&gBIT30)
			return fault_overload;
		break;
	default :
		break;
	}
	return fault_Normal;
}
