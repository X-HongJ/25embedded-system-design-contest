/* 以下是写指令或显示数据函数
		 Flag=1写入的是显示数据
		 Flag=0写入的是指令数据
*/
void Write_Command_or_Data(unsigned char Dat,unsigned char Flag)
{
	unsigned char j;					//用于循环写入8位数据
	CS_0();										//使能片选

	if(Flag==1)								//判断要发送的是指令还数据
	{
		AS_1();									//选择数据模式
	}
	else
	{
		AS_0();									//选择指令模式
	}

	for(j=0;j<8;j++)					//准备写入8位
	{
		if((Dat&0x80)==0x80)		//判断最高位是0还是1 （spi接口发送数据始终是高位优先）
		{
			SDA_1();							//如果是1 则拉高SDA线
		}
		else
		{
			SDA_0();							//否则就是0 拉低SDA线
		}

		SCL_0();								//拉低时钟线 准备写入数据
		DELAY_US(1);
		SCL_1();								//产生上升沿 写入数据
		DELAY_US(1);

		Dat<<=1;								//数据左移一位，准备些下一位数据
	}
	CS_1();										//写入完毕，取消片选
}
/* 设置列地址 */
/* Command代表指令 */
void Column_set(unsigned char L)
{
	Write_Command_or_Data((0x10|(L>>4)),Command);		//发送列地址的第一个字节
	Write_Command_or_Data((0x00|(L&0x0f)),Command);		//发送列地址的第二个字节（这个一个双字节指令）
}

/* 设置页地址 */
/* Command代表指令 */
void Page_set(unsigned char Y)
{
	Write_Command_or_Data((0xb0+Y),Command);			//发送页地址函数
}
/*数据显示函数*/
void Picture_display(const unsigned char *p) //改成了只显示中间四页
{
	int Y,L;					//使用局部变量定义页地址和列地址
	for(Y=7-2;Y>=0+2;Y--)   //changed      	//从第一页开始写显示数据
	{
		Page_set(Y);					//设置页地址 共有8个页地址 每次写完一页后切换地址
		Column_set(4);	  				//设置列地址 共有128个列地址 每次写完128列后切换地址
		for(L=0;L<128;L++)				//从第一列开始写显示数据
		{
			Write_Command_or_Data(*p++,Data);		//写入显示数据 指针p++表示提取下一位数据（原则是先提取，后执行++）
		}
	}
}
void Screen_clear(u8 num1,u8 num2)
{
	unsigned char page,column;
	for(page=num1;page<num2+1;page++)             //page loop
	{
		Page_set(page);
		Column_set(4);
		for(column=0;column<128;column++)	//column loop
		{
			Write_Command_or_Data(0x00,Data);
		}
	}
}
